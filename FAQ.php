      <h2 class="FAQ_h2">Qu’est-ce qu’E2N ?</h2>
      <div>
        <p>
          E2N (Ecole du Numérique du Noyonnais) est une structure de formation, créée et portée par NOVEI Formation (SARL de droit privé)
          sur le modèle de Simplon.co (programme et méthodes pédagogiques). L’Ecole propose une formation intensive de 6 mois (35 heures par semaine)
          aux métiers du Code et du Numérique, notamment celui de Développeur-se Web.<br /><br />
          L'apprentissage est fondé sur la pratique par projet, la programmation en binôme ainsi que la mise en situation de la transmission
          et l’autonomie.
        </p>
      </div>
      <h2 class="FAQ_h2">Qu'est-ce que le label « Grande École du Numérique » ?</h2>
      <div>
        <p>
          E2N a été labellisée « GEN » en février dernier dans le cadre de l’appel à projet lancé en octobre 2015 qui a pu récompenser 171
          formations. Dispensées dans des « fabriques du Numérique » ces formations courtes et qualifiantes aux métiers du Numérique permettront
          à un large public de se former efficacement afin de trouver du travail rapidement dans cette filière d'avenir.<br /><br />
          Ce projet collectif est porté par quatre ministres : Najat Vallaud-Belkacem, ministre de l’Education nationale et de
          l’Enseignement supérieur, Myriam El Khomri, ministre du Travail, de l’Emploi, de la Formation professionnelle et du
          Dialogue social, Patrick Kanner, ministre de la Ville, de la Jeunesse et des Sports et Axelle Lemaire, secrétaire d’Etat
          au Numérique.<br /><br />
          Le label « Grande Ecole du Numérique » permet aux formations sélectionnées d’être valorisées, soutenues financièrement
          et accompagnées dans leurs démarches de mobilisationdes financements de droit commun.
        </p>
      </div>
      <h2 class="FAQ_h2">Qu'est ce que le métier de développeur WEB ?</h2>
      <div>
        <p>
          Le développeur WEB réalise l’ensemble des fonctions d’un site ou d’une application WEB. Celui-ci va traduire les
          besoins du client en code informatique, et ce en utilisant un langage de programmation.<br /><br />
          Il travaille sous la responsabilité d’un chef de projet.
        </p>
      </div>
      <h2 class="FAQ_h2">Quels sont les débouchés ?</h2>
      <div>
        <p>
          A la fin de cette formation, les apprenant(e)s pourront prétendre aux métiers suivants :<br /><br />
          - Intégrateur WEB, UX designer : Il crée la maquette fonctionnelle en se basant sur la maquette graphique.<br />
          - Développeur front-end junior : Il s’occupe du design du site et de tout ce qui va interagir avec l’utilisateur.<br />
          - Développeur back-end junior : Contrairement au développeur front-end, il gère la partie non visible par l’utilisateur
          c’est à dire la partie serveurs, base de données ...<br />
        </p>
      </div>
      <h2 class="FAQ_h2">A qui s'adresse cette formation ?</h2>
      <div>
        <p>
          Elle est ouverte à tous, et prioritairement aux personnes éloignées de l’emploi et de la formation résidant du Pays
          Noyonnais : jeunes, femmes (insuffisamment représentées dans les métiers du Numérique), demandeurs d’emplois, personnes
          en reconversion professionnelle, habitants des Quartiers Politique de la Ville ...<br /><br />
          Aucun diplôme ou expérience ne sont pré-requis ; l’intégration à la formation est fondée sur l’appétence au Numérique
          et surtout, sur une forte motivation.
        </p>
      </div>
      <h2 class="FAQ_h2">La formation est-elle rémunérée ? </h2>
      <div>
        <p>
          E2N propose une formation développeur Web entièrement gratuite pour ses apprenants.
          Par conséquent la formation n'est pas rémunérée.
        </p>
